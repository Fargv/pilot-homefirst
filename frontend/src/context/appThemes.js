export const APP_THEME_IDS = APP_THEMES.map((theme) => theme.id);

export function getAppTheme(themeId) {
  return APP_THEMES.find((theme) => theme.id === themeId) || APP_THEMES[0];
}

export function isAppThemeId(themeId) {
  return APP_THEME_IDS.includes(String(themeId || ""));
}

export function isThemeAvailableForPlan(themeId, plan = "basic") {
  const theme = getAppTheme(themeId);
  return theme.availableForPlans.includes(String(plan || "basic").toLowerCase());
}

export function isBasicTheme(themeId) {
  return getAppTheme(themeId).availableForPlans.includes("basic");
}

/** The Basic-tier default for a given mode (Periwinkle light / Jet Stream dark). */
export function getDefaultThemeIdForMode(mode) {
  return mode === "dark" ? DEFAULT_DARK_THEME_ID : DEFAULT_THEME_ID;
}

/**
 * Resolve a stored theme against the user's access. Premium themes a
 * Basic user can't reach fall back to the Basic default of the SAME mode,
 * so a downgraded "Deep Ocean" lands on Jet Stream, not on a light theme.
 */
export function resolveThemeIdForAccess(themeId, canUsePremiumThemes = false) {
  if (!isAppThemeId(themeId)) return DEFAULT_THEME_ID;
  const theme = getAppTheme(themeId);
  if (canUsePremiumThemes || isBasicTheme(theme.id)) return theme.id;
  return getDefaultThemeIdForMode(theme.mode);
}

/** First-run preference when nothing is stored: follow the OS color scheme. */
export function getSystemPreferredThemeId() {
  try {
    if (typeof window !== "undefined" && window.matchMedia
      && window.matchMedia("(prefers-color-scheme: dark)").matches) {
      return DEFAULT_DARK_THEME_ID;
    }
  } catch {}
  return DEFAULT_THEME_ID;
}
